package desafiomv;

public class Food {
	
	private int id;
	private String nome;
	private int id_colab;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getId_colab() {
		return id_colab;
	}
	public void setId_colab(int id_colab) {
		this.id_colab = id_colab;
	}

}
